struct Time{
	int hour;
	int min;
	int sec;
};
void main(){
	struct Time s1,s2;
	
	printf("hour:");
	scanf("%d",&s1.hour);
	printf("min:");
	scanf("%d",&s1.min);
	printf("sec:");
	scanf("%d",&s1.sec);
	printf("######\n");
	printf("hour:");
	scanf("%d",&s2.hour);
	printf("min:");
	scanf("%d",&s2.min);
	printf("sec:");
	scanf("%d",&s2.sec);
	
} 